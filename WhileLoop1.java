package com.tiwarisaish.advancedjava;

public class WhileLoop1 {
    public static void main(String[] args) {

        int start =1;
        int stop = 5;

        while (start<=stop){
            System.out.println(start);
            start++;
        }
        System.out.println(start);
        System.out.println(stop);
    }
}
